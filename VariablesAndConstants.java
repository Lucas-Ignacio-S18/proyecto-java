public class VariablesAndConstants {
        public static void main(String[] args) {
            
            String name = "Lucas"; //Creamos variable Tipo String | asignamos etiqueta o nombre = Le damos el valor.
            System.out.println("Mi nombre es "+ name); //Imprimimos la variable que creamos.

            name = "Ignacio";
            System.out.println("Ahora mi nombre es " + name); //Imprimimos la variable NAME pero con el valor cambiado.

            int age = 24; //Variable tipo INT + NOMBRE = VALOR .
            System.out.println("Mi edad es " + age); //imprimo la edad y le agrego uun string explicando que significa el valor.
            

        }
}
